using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Racunar.Classes
{
    class Operators
    {
        
     

        public static double  Sabiranje(double one, double two)
        {
            return one + two;
        }

        public static double Oduzimanje(double one, double two)
        {
            return one - two;
        }

        public static double Deljenje(double one, double two)
        {
            return one / two;
        }

        public static double Mnozenje(double one, double two)
        {
            return one * two;
        }

       
    }
}
